public class Usuario {
    public String id;
    public String nombre;
    public String apellido;
    public String dni;

    public Usuario(String id, String nombre, String apellido, String dni) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.dni = dni;
    }
}
