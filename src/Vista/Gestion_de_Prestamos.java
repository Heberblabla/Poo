    private void boton_realizar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_realizar_ActionPerformed
        String id = ids_txt_.getText();
        Material mat = obtenerMaterialPorID(id);

        if (mat == null) {
            JOptionPane.showMessageDialog(null, "Material no encontrado.");
            return;
        }

        if (!(mat instanceof IPrestable)) {
            JOptionPane.showMessageDialog(null, "Este material NO se puede prestar.");
            return;
        }

// Datos del usuario
        String nombre = caja_nombre_.getText();
        String apellido = caja_edad_.getText();
        String dni = caja_dni_.getText();

// ----------------------
// VALIDACIÓN DE DATOS
// ----------------------
        nombre = nombre.trim();
        apellido = apellido.trim();
        dni = dni.trim();

        if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se permiten campos vacíos.");
            return;
        }

        if (!dni.matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "El DNI debe contener solo números.");
            return;
        }

        if (dni.length() != 8) {
            JOptionPane.showMessageDialog(null, "El DNI debe tener exactamente 8 dígitos.");
            return;
        }
// ----------------------

        IPrestable p = (IPrestable) mat;

// Intentar préstamo
        String resultado = p.prestar(nombre, apellido, dni);

        // ---------------------------
        //   MANEJAR RESULTADOS
        // ---------------------------
        // Usuario no encontrado
        if (resultado.equalsIgnoreCase("usuario no encontrado")) {

            int opcion = JOptionPane.showConfirmDialog(
                    null,
                    "El usuario no existe.\n¿Desea registrarlo?",
                    "Usuario no encontrado",
                    JOptionPane.YES_NO_OPTION
            );

            if (opcion == JOptionPane.YES_OPTION) {

                Funciones_BD dao = new Funciones_BD();
                String r = dao.InsertarUsuario(nombre, apellido, dni);

                if (r.equalsIgnoreCase("OK")) {
                    // Volver a intentar el préstamo
                    String r2 = p.prestar(nombre, apellido, dni);

                    if (r2.equalsIgnoreCase("OK")) {
                        JOptionPane.showMessageDialog(null, "Usuario registrado y préstamo exitoso.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Usuario creado pero el préstamo falló: " + r2);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo registrar el usuario.");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Operación cancelada.");
            }

            return;
        }

        // Préstamo exitoso
        if (resultado.equalsIgnoreCase("OK")) {
            JOptionPane.showMessageDialog(null, "Préstamo exitoso.");
            Funciones_BD actus = new Funciones_BD();
            actus.actu();
            cargarTabla();
            return;
        }

        // Cualquier otro error
        JOptionPane.showMessageDialog(null, "No se pudo realizar el préstamo: " + resultado);
       
    }//GEN-LAST:event_boton_realizar_ActionPerformed

