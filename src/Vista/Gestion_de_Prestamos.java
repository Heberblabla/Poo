    private void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tabla_muestra_.getModel();
        modelo.setRowCount(0); // Limpia la tabla

        for (Material m : Cargar_Elementos.materiales) {
            modelo.addRow(new Object[]{
                m.id,
                m.titulo,
                m.autor,
                m.anio
            });
        }
    }

    private void cargarTablaConLista(ArrayList<Material> lista) {
        DefaultTableModel modelo = (DefaultTableModel) tabla_muestra_.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        for (Material m : lista) {
            modelo.addRow(new Object[]{
                m.id,
                m.titulo,
                m.autor,
                m.anio
            });
        }
    }

    private void filaSeleccionada() {
        int fila = tabla_muestra_.getSelectedRow();

        if (fila == -1) {
            return; // nada seleccionado
        }
        String id = tabla_muestra_.getValueAt(fila, 0).toString();
        String titulo = tabla_muestra_.getValueAt(fila, 1).toString();
        String autor = tabla_muestra_.getValueAt(fila, 2).toString();
        String anio = tabla_muestra_.getValueAt(fila, 3).toString();
        // Ejemplo: llenar cajas de texto
        Titulo.setText(titulo);
        Autor.setText(autor);
        Año.setText(anio);
        ids_txt_.setText(id);

        Material waos = obtenerMaterialPorID(id);
        String descrip = waos.descripcion;
        Disponibilidad_xd.setState(waos.disponibilidad);
        titulo_txt_libor.setText(waos.cla);
    }

    private Material obtenerMaterialPorID(String id) {
        return Cargar_Elementos.materiales.stream()
                .filter(m -> m.id.equalsIgnoreCase(id))
                .findFirst()
                .orElse(null);
    }


